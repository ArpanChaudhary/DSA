public class Var1{
    public static void main(String[] args) {
        int a = 10;
        float b = (float) 0.5;
        char c = 'a';
        double d = 200.09;

        System.out.println("Integer: " + a);
        System.out.println("Float value: " + b);
        System.out.println("Character is: " + c);
        System.out.println("Double: " + d);

    }
}