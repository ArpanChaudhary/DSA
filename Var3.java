// Declare two variables as a and b. Initialize the values to those variables as 1042 and 7 respectively.

// Declare another two variables quo and rem that will store the quotient and 
// remainder of these numbers taking a as dividend and b as divisor.

// Print quo and rem in a line separated by a space.



public class Var3{
    public static void main(String[] args){

        int a = 1042;
        int b = 7;

        int quo = a / b;
        int rem = a % b;

        System.out.println(quo + " " + rem);
    }
}